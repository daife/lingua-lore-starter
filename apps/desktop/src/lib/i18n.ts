export type TranslationKey = keyof typeof translations.en;

export function translate(language: AppLanguage, key: TranslationKey, value?: string) {
  const entry = translations[language][key] ?? translations.en[key];
  return typeof entry === "function" ? entry(value ?? "") : entry;
}

export function defaultAppLanguage(): AppLanguage {
  if (typeof window === "undefined") {
    return "en";
  }
  const stored = window.localStorage.getItem("lingua-lore-app-language");
  if (stored === "zh" || stored === "ja" || stored === "en") {
    return stored;
  }
  return "en";
}
